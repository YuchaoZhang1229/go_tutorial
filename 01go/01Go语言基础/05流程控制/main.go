func forDemo() {
	for i := 0; i < 10; i++ {
		fmt.Print(i)
	}
}

func main() {
	ifDemo1()
	switchDemo1()
	switchDemo2()
	switchDemo3()
	switchDemo4()
	forDemo()
}
